function maiorNumero(a,b){
    if (a > b){
        console.log(`O número ${a} é maior que o número ${b}.`);
    } else if (b > a){
        console.log(`O número ${b} é maior que o número ${a}.`);
    } else if (a = b){
        console.log(`Os números ${a} e ${b} são iguais.`);
    }
}

maiorNumero(58, 9);